    print(f"ü§ñ AI Model: {Config.AI_MODEL}")
    print(f"üìä Max Tokens: {Config.AI_MAX_TOKENS}")
    print(f"üå°Ô∏è Temperature: {Config.AI_TEMPERATURE}")
    print("=====================================\n")
    
    test_results = {}
    
    # Run all tests
    test_functions = [
        ("Connection Test", test_ai_connection),
        ("Persian Support", test_persian_support),
        ("English Support", test_english_support),
        ("Arabic Support", test_arabic_support),
        ("Shop Support", test_shop_specific_support),
        ("Admin Assistance", test_admin_assistance),
        ("Intent Analysis", test_user_intent_analysis),
        ("Content Suggestions", test_content_suggestions),
        ("Quick Replies", test_quick_replies),
        ("Conversation Flow", test_conversation_flow),
        ("Feature Explanation", test_feature_explanation)
    ]
    
    for test_name, test_func in test_functions:
        try:
            result = await test_func()
            test_results[test_name] = result
        except Exception as e:
            print(f"‚ùå {test_name}: FAILED with error: {e}")
            test_results[test_name] = False
        
        # Small delay between tests
        await asyncio.sleep(1)
    
    # Summary
    print("\n" + "="*50)
    print("üèÅ TEST RESULTS SUMMARY")
    print("="*50)
    
    passed = 0
    total = len(test_results)
    
    for test_name, result in test_results.items():
        status = "‚úÖ PASSED" if result else "‚ùå FAILED"
        print(f"{test_name}: {status}")
        if result:
            passed += 1
    
    print(f"\nüìä Overall: {passed}/{total} tests passed ({passed/total*100:.1f}%)")
    
    if passed == total:
        print("üéâ ALL TESTS PASSED! AI is fully trained and ready!")
    elif passed >= total * 0.8:
        print("üåü Most tests passed! AI is well-trained!")
    elif passed >= total * 0.6:
        print("‚ö†Ô∏è Some issues detected. AI needs minor adjustments.")
    else:
        print("üö® Multiple failures. AI needs significant work.")
    
    return test_results

